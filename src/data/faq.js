function hasPlaygroupText(value = "") {
  return /playgroup/i.test(String(value || ""));
}

export function stripPlaygroupFromTabs(tabs = []) {
  return (Array.isArray(tabs) ? tabs : [])
    .map((tab) => {
      const safeSections = (Array.isArray(tab?.sections) ? tab.sections : [])
        .filter((section) => !hasPlaygroupText(section?.heading))
        .map((section) => ({
          ...section,
          items: (Array.isArray(section?.items) ? section.items : []).filter((item) => {
            const text = `${item?.q || ""} ${item?.a || ""}`;
            return !hasPlaygroupText(text);
          }),
        }))
        .filter((section) => Array.isArray(section.items) && section.items.length > 0);

      const safeItems = (Array.isArray(tab?.items) ? tab.items : []).filter((item) => {
        const text = `${item?.q || ""} ${item?.a || ""}`;
        return !hasPlaygroupText(text);
      });

      return {
        ...tab,
        sections: safeSections,
        items: safeItems,
      };
    })
    .filter((tab) => {
      if (hasPlaygroupText(tab?.label)) return false;
      const sectionCount = Array.isArray(tab?.sections) ? tab.sections.length : 0;
      const itemCount = Array.isArray(tab?.items) ? tab.items.length : 0;
      return sectionCount > 0 || itemCount > 0;
    });
}

export function stripPlaygroupFromFaqItems(items = []) {
  return (Array.isArray(items) ? items : []).filter((item) => {
    const text = `${item?.q || ""} ${item?.a || ""}`;
    return !hasPlaygroupText(text);
  });
}

export function flattenFaqItemsFromTabs(tabs = []) {
  const entities = [];
  (Array.isArray(tabs) ? tabs : []).forEach((tab) => {
    (Array.isArray(tab?.sections) ? tab.sections : []).forEach((section) => {
      (Array.isArray(section?.items) ? section.items : []).forEach((item) => {
        if (!item?.q || !item?.a) return;
        entities.push({
          "@type": "Question",
          name: item.q,
          acceptedAnswer: {
            "@type": "Answer",
            text: item.a,
          },
        });
      });
    });
    (Array.isArray(tab?.items) ? tab.items : []).forEach((item) => {
      if (!item?.q || !item?.a) return;
      entities.push({
        "@type": "Question",
        name: item.q,
        acceptedAnswer: {
          "@type": "Answer",
          text: item.a,
        },
      });
    });
  });
  return entities;
}
